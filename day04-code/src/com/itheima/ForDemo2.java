package com.itheima;

public class ForDemo2 {
    public static void main(String[] args) {
        //需求1：打印1-5
        for (int i = 1; i <= 5 ; i++) {
            //第一次循环：i = 1;
            System.out.println(i);
        }
        System.out.println("================");
        //需求2：打印5-1
        for (int i = 5; i >= 1 ; i--) {
            System.out.println(i);
        }

    }
}
